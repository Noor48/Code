plt.plot(datas2[:,0], datas2[:,4], color='mediumaquamarine', linewidth=2,ls='-.')
plt.plot(datas2[:,0], datas2[:,5], label='O', color='darkgoldenrod', linewidth=2,ls=':')
plt.plot(datas2[:,0], datas2[:,6], color='darkgoldenrod', linewidth=2,ls=':')
plt.ylim([-20,20])
plt.xlim([-20,10])
plt.axhline(0, c='black', alpha=0.3, linestyle='-')
plt.axvline(0, c='black', alpha=0.3, linestyle='-.')
plt.legend()
#plt.title("InFeO3", fontsize=40)
plt.xlabel("Energy (ev)", fontsize=20)
plt.ylabel("Density of States (arbitrary)", fontsize=20)
plt.show()

