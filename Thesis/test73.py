import matplotlib.pyplot as plt
import numpy as np
import matplotlib


datas1=np.loadtxt("TDOSFe.dat",dtype=np.float64,skiprows=1) 
datas2=np.loadtxt("PDOS_USERFe.dat",dtype=np.float64,skiprows=1)

f = plt.figure()
f.set_figwidth(10)
f.set_figheight(8)
plt.plot(datas1[:,0], datas1[:,1], label="InFeO3", color='darkslateblue', linewidth=2, ls='-')
plt.plot(datas1[:,0], datas1[:,2], color='darkslateblue', linewidth=2, ls='-')
plt.plot(datas2[:,0], datas2[:,1], label='In', color='mediumpurple', linewidth=2,ls='--')
plt.plot(datas2[:,0], datas2[:,2], color='mediumpurple', linewidth=2,ls='--')
