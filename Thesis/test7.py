import matplotlib.pyplot as plt
import numpy as np


datas1=np.loadtxt("TDOS.dat",dtype=np.float64,skiprows=1) 
datas2=np.loadtxt("PDOS_USER.dat",dtype=np.float64,skiprows=1) 

f = plt.figure()
f.set_figwidth(10)
f.set_figheight(8)

