plt.plot(datas2[:,0], datas2[:,4], linewidth=1, color='cornflowerblue', alpha=0.5, linestyle='--')
plt.fill_between(datas2[:,0], datas2[:,5], label='up In all', color='orange', alpha=0.3)
plt.plot(datas2[:,0], datas2[:,5], linewidth=1, color='orange', alpha=0.5)
plt.fill_between(datas2[:,0], datas2[:,6], label='up In all', color='orange', alpha=0.2, linestyle='--')
plt.plot(datas2[:,0], datas2[:,6], linewidth=1, color='orange', alpha=0.5, linestyle='--')
plt.legend()
plt.title("Density of States", fontsize=40)
plt.xlabel("Energy (ev)", fontsize=20)
plt.ylabel("states/eV", fontsize=20)
plt.show()

